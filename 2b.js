function ndiffs(str1, str2) {
    let s1 = str1.split('');
    let s2 = str2.split('');
    let matches = 0;
    let result = [];
    for (let i = 0; i < s1.length; i++)
        if (s1[i] === s2[i]) {
            matches++;
            result.push(s1[i]);
        }
    return { count: matches, str: result.join('') };
}

